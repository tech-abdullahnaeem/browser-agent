# ── Planning prompt ──────────────────────────────────────────────────────────
# Sent to Gemini Pro when the task is deemed complex, to decompose it into
# high-level sub-goals *before* the step-by-step agent loop starts.

PLANNING_PROMPT = """\
You are a QA test planner for an end-to-end browser testing agent that tests websites \
and web apps generated by AI agents.

Given the user's QA testing task below, create a thorough test plan that covers the \
ENTIRE site — not just one page. Think like a QA engineer doing a full regression test.

Your plan should follow this structure:
1. DISCOVER — Map the site (use crawl_and_map_site to find all pages & interactive elements)
2. PAGE-BY-PAGE TESTING — For each discovered page:
   - Navigate to the page and verify it loads
   - Check visual rendering and content
   - Test all interactive elements (buttons, forms, tabs, etc.)
   - Run accessibility and console error checks
3. CROSS-PAGE TESTING — Test navigation flows between pages:
   - Menu/nav links work correctly
   - Back/forward navigation
   - Any multi-step flows (signup → confirmation, etc.)
4. INTERACTION TESTING — For each form, button, and interactive element:
   - Test with valid inputs (happy path)
   - Test with invalid/empty inputs (error path)
   - Verify feedback/response
5. FINAL AUDIT — Run comprehensive checks and compile report

Keep the plan concise (5-15 steps) but ensure it covers ALL pages and interactions.
For AI-generated sites, specifically plan to check for: placeholder content, non-functional \
UI elements, broken routing, hardcoded data, and missing error handling.

User task:
{task}

{context_section}

Respond ONLY with the numbered list, nothing else.
"""

